function categorize(counterparty) {
  if (!counterparty) return '기타';
  for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    if (keywords.some(k => counterparty.includes(k))) {
      return category;
    }
  }
  return '기타';
}

